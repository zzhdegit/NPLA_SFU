# 2. 定点转换核心逻辑
# ---------------------------------------------------------
def float_to_signed_hex(val, frac_bits, total_bits=16):
    scaled_val = int(round(val * (1 << frac_bits)))
    max_val = (1 << (total_bits - 1)) - 1
    min_val = -(1 << (total_bits - 1))
    scaled_val = max(min(scaled_val, max_val), min_val)

    # 转为补码以便生成 Hex
    hex_val = scaled_val if scaled_val >= 0 else (1 << total_bits) + scaled_val
    return f"{total_bits}'sh{hex_val:04X}"


